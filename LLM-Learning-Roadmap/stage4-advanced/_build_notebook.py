    "            remaining = messages[1:]",
    "        else:",
    "            remaining = messages",
    "",
    "        # Keep first user turn (sets context)",
    "        first_user_idx = next((i for i, m in enumerate(remaining) if m.get('role') == 'user'), None)",
    "",
    "        # Keep last N turns (N = however many fit)",
    "        available_tokens = self.max_tokens - self.count_tokens(result)",
    "        recent = []",
    "        for msg in reversed(remaining):",
    "            msg_tokens = self.token_estimate_fn(msg.get('content', ''))",
