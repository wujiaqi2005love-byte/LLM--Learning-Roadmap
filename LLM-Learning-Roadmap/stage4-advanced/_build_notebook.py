    "",
    "class VRAMMonitor:",
    "    \"\"\"Track VRAM usage over time to detect leaks.\"\"\"",
    "",
    "    def __init__(self):",
    "        self.snapshots = []",
    "        self.baseline = None",
    "",
    "    def take_snapshot(self):",
    "        \"\"\"Record current VRAM state.\"\"\"",
    "        if not torch.cuda.is_available():",
    "            return {'allocated': 0, 'reserved': 0, 'timestamp': time.time()}",
    "        snap = {",
    "            'allocated': torch.cuda.memory_allocated() / 1024**2,  # MB",
    "            'reserved': torch.cuda.memory_reserved() / 1024**2,",
    "            'max_allocated': torch.cuda.max_memory_allocated() / 1024**2,",
    "            'timestamp': time.time(),",
    "        }",
    "        self.snapshots.append(snap)",
    "        return snap",
    "",
    "    def set_baseline(self):",
    "        \"\"\"Set the baseline snapshot (call after model load, before serving).\"\"\"",
    "        self.baseline = self.take_snapshot()",
    "        print(f'[VRAM Baseline] {self.baseline[\"allocated\"]:.0f} MB allocated')",
    "",
    "    def check_leak(self, threshold_mb=100, window=20):",
    "        \"\"\"",
