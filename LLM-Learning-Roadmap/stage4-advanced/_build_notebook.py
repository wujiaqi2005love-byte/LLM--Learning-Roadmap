    "        Returns True if a leak is suspected.",
    "        \"\"\"",
    "        if len(self.snapshots) < window + 1:",
    "            return False",
    "",
    "        recent = self.snapshots[-window:]",
    "        start_mem = recent[0]['allocated']",
    "        end_mem = recent[-1]['allocated']",
    "        growth = end_mem - start_mem",
    "",
    "        if growth > threshold_mb:",
    "            print(f'[VRAM LEAK WARNING] +{growth:.0f} MB over last {window} steps')",
    "            print(f'  Start: {start_mem:.0f} MB → End: {end_mem:.0f} MB')",
    "            return True",
    "        return False",
    "",
    "    def plot_history(self):",
    "        \"\"\"Visualize VRAM usage over time.\"\"\"",
    "        if len(self.snapshots) < 2:",
